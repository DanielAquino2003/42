#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int		i;
	char	salto;

	salto = '\n';
	i = 0;
	while (s[i] != '\0')
	{
		write(fd, &s[i], 1);
		i ++;
	}
	write(fd, &salto, 1);
}
