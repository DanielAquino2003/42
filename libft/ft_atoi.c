#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	res;
	int	i;
	int	flag;

	i = 0;
	res = 0;
	flag = 1;
	while (nptr[i] == ' ' || nptr[i] == '\n' || nptr[i] == '\t'
		|| nptr[i] == '\v' || nptr[i] == '\f' || nptr[i] == '\r')
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			flag *= -1;
		i ++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		res = res * 10 + (nptr[i] - '0');
		i ++;
	}
	return (res *= flag);
}
